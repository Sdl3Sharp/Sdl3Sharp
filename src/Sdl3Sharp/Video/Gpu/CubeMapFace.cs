namespace Sdl3Sharp.Video.Gpu;

/// <summary>
/// Repesents a face of a cube map
/// </summary>
public enum CubeMapFace
{
	/// <summary>The positive X face of the cube map</summary>
	PositiveX,

	/// <summary>The negative X face of the cube map</summary>
	NegativeX,

	/// <summary>The positive Y face of the cube map</summary>
	PositiveY,

	/// <summary>The negative Y face of the cube map</summary>
	NegativeY,

	/// <summary>The positive Z face of the cube map</summary>
	PositiveZ,

	/// <summary>The negative Z face of the cube map</summary>
	NegativeZ
}
